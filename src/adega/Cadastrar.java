    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Bebidas bebida = new Bebidas();
        Banco banco = new Banco();
        String atributo;
        String nome = jNome.getText();
        double preco = Double.parseDouble(jPreco.getText());
        bebida.setNome(nome);
        bebida.setPreco(preco);
        
      
        if(jRadioButton1.isSelected()){
            bebida.setTipo("vinho");
            atributo=(String)jComboBox1.getSelectedItem();
            bebida.setPreco(preco);
            bebida.setAtributo(atributo);
        }
        if(jRadioButton2.isSelected()){ 
            bebida.setTipo("refrigerante");
            atributo=(String)jComboBox2.getSelectedItem();
            bebida.setPreco(preco);
            bebida.setAtributo(atributo);
        }
        if(jRadioButton3.isSelected()){
            bebida.setTipo("suco");
            atributo=(String)jComboBox3.getSelectedItem();
            bebida.setPreco(preco);
            bebida.setAtributo(atributo);
        }
        
        try {
            banco.Cadastrar(bebida);
        } catch (SQLException ex) {
            System.err.println("Erro"+ex);
        } catch (ClassNotFoundException ex) {
            System.err.println("Erro"+ex);
        }
        JOptionPane.showMessageDialog(null, "Bebida cadastrada");
        jNome.setText("");
        jPreco.setText("");
        
    }

