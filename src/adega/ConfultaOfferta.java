    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Banco banco = new Banco();
        Bebidas bebida = new Bebidas();
        List<Bebidas> lista = null;
        String tipo = (String) jComboBox1.getSelectedItem();
        bebida.setTipo(tipo);
        try {
            lista = banco.consultarTodos();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ConfultaOfferta.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ConfultaOfferta.class.getName()).log(Level.SEVERE, null, ex);
        }
        bebida.setTipo(tipo);

        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        modelo.setRowCount(0);

        String tipoSelecionado = (String) jComboBox1.getSelectedItem();
        int contador = 0; 
        for(Bebidas b : lista){
            if (b.getTipo().equals(tipoSelecionado)) {
                boolean emOferta = false;
                if (tipo.equalsIgnoreCase("vinho") && b.getPreco() < 50.00) {
                    emOferta = true;
                } else if (tipo.equalsIgnoreCase("suco") && b.getPreco() < 10.00) {
                    emOferta = true;
                } else if (tipo.equalsIgnoreCase("refrigerante") && b.getPreco() < 5.00) {
                    emOferta = true;
                }

                if (emOferta) {
                    modelo.addRow(new Object[]{ b.getNome(), b.getPreco(), b.getAtributo() });
                    contador= contador + 1;                
                } 
           }
        }      
        if(contador == 0) {
                JOptionPane.showMessageDialog(null,"Produtos sem ofertas!");
        } else {
            JOptionPane.showMessageDialog(null,"Produtos com ofertas!!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

